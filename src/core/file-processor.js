// fetch discovered ids and their metadata
import { discoverIds } from './id-discoverer.js';
import { fetchMetadata } from './metadata-fetcher.js';
import { readConfig } from './config-reader.js';
import { generateHeadHtml } from './head-generator.js';
import { injectScriptInTemplate } from "./template-injector.js";
import fs from 'fs';
import path from 'path';

export async function processFiles() {
    try {
        const config = await readConfig();
        const summary = {
            timestamp: new Date().toISOString(),
            pages: [],
            totalMetadataEntries: 0,
            outputDirectories: []
        };
        
        for (let i = 0; i < config.pages.length; i++) {
            const page = config.pages[i];
            console.log(`\n📄 Processing page: ${page.route}`);
            
            const ids = await discoverIds(i);
            console.log(`   📊 Found ${ids.length} content IDs`);
            
            const routeName = page.route.split('/')[1];
            // Determine base directory based on environment
            const baseDir = process.env.NODE_ENV === 'production' 
                ? path.join(process.cwd(), 'dist')
                : path.join(process.cwd(), 'test', 'fixtures', 'postBuild');
            
            // Create param directory path (FIXED: define paramDir)
            const paramDir = path.join(baseDir, routeName, '_param');
            await fs.promises.mkdir(paramDir, { recursive: true });
            
            // Inject script into template
            const templatePath = path.join(paramDir, 'index.html');
            await injectScriptInTemplate(templatePath);

            // Collect all metadata in one object
            const metadataObject = {};
            const pageResults = [];
            
            for (const id of ids) {
                try {
                    const metadata = await fetchMetadata(i, id);
                    const metadataObj = Array.isArray(metadata) ? metadata[0] : metadata;
                    
                    // Store in the metadata object with ID as key
                    metadataObject[id] = metadataObj;
                    
                    pageResults.push({ 
                        id, 
                        metadata: metadataObj
                    });
                    
                    if (pageResults.length % 10 === 0) {
                        console.log(`   ✅ Processed ${pageResults.length}/${ids.length} items`);
                    }
                    
                } catch (error) {
                    console.error(`   ❌ Failed to process ID ${id}:`, error.message);
                }
            }
            
            // Generate metadata.js content
            const metadataJsContent = generateMetadataJs(metadataObject);
            const metadataJsPath = path.join(paramDir, 'metadata.js');
            await fs.promises.writeFile(metadataJsPath, metadataJsContent, 'utf-8');
            
            console.log(`   ✅ Generated metadata.js with ${Object.keys(metadataObject).length} entries`);
            
            summary.pages.push({
                route: page.route,
                idsFound: ids.length,
                metadataEntries: Object.keys(metadataObject).length,
                metadataFilePath: metadataJsPath,
                items: pageResults
            });
            
            summary.totalMetadataEntries += Object.keys(metadataObject).length;
            summary.outputDirectories.push(paramDir);
        }
        
        return summary;
        
    } catch (error) {
        console.error('❌ Error processing files:', error.message);
        throw error;
    }
}

// Helper function to generate metadata.js content
function generateMetadataJs(metadataObject) {
    // Convert the metadata object to a formatted JSON string
    const metadataJson = JSON.stringify(metadataObject, null, 2);
    
    // Create the JavaScript file that sets window.METADATA
    return `// Metadata generated by weweb-metadata on ${new Date().toISOString()}
window.METADATA = ${metadataJson};`;
}

// Run if called directly
if (import.meta.url === `file://${process.argv[1]}`) {
    processFiles().then(result => {
        console.log('\n✅ Metadata processing complete!');
        console.log('\n📊 Summary:');
        console.log(`   🕒 Timestamp: ${result.timestamp}`);
        console.log(`   📄 Pages processed: ${result.pages.length}`);
        console.log(`   📊 Total metadata entries: ${result.totalMetadataEntries}`);
        console.log('\n   📁 Output directories:');
        result.outputDirectories.forEach(dir => {
            console.log(`      - ${dir}`);
        });
        
        console.log('\n   📋 Page details:');
        result.pages.forEach(page => {
            console.log(`      ${page.route}: ${page.metadataEntries}/${page.idsFound} entries in metadata.js`);
        });
        
    }).catch(error => {
        console.error('❌ Error in file processing:', error.message);
    });
}

processFiles();